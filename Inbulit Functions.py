#INBUILT FUNCTION IN PYTHON


#LEN (length of a string)
name = "Priyadharshan"
print(len(name))

#TYPE (Tell us the data type)
print(type(name))

#DIVMOD (Gives the value of division and remainder)
a = 6
b = 2
print(divmod(a,b))

#ABS (Gives the absolute values)
num = -68
print(abs(num))

#POW (Raises the power of number)
print(pow(6,2))

#HELP (Will help you by giving the details)
print(help(pow))
